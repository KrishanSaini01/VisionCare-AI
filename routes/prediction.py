import os

from flask import (
    Blueprint,
    render_template,
    request,
    redirect,
    url_for,
    flash
)

from flask_login import (
    login_required,
    current_user
)

from werkzeug.utils import secure_filename

from extensions import db

from database.models import Prediction

from model.predict import predict_image


prediction = Blueprint(
    "prediction",
    __name__
)


UPLOAD_FOLDER = "static/uploads"

ALLOWED_EXTENSIONS = {
    "jpg",
    "jpeg",
    "png"
}


def allowed_file(filename):

    return "." in filename and \
        filename.rsplit(".", 1)[1].lower() in ALLOWED_EXTENSIONS


@prediction.route("/upload", methods=["GET", "POST"])
@login_required
def upload():

    if request.method == "POST":

        if "image" not in request.files:

            flash("Please select an image.", "danger")

            return redirect(request.url)

        file = request.files["image"]

        if file.filename == "":

            flash("Please choose an image.", "warning")

            return redirect(request.url)

        if not allowed_file(file.filename):

            flash("Only JPG, JPEG and PNG images are allowed.", "danger")

            return redirect(request.url)

        # -------------------------
        # Save Image
        # -------------------------

        os.makedirs(UPLOAD_FOLDER, exist_ok=True)

        filename = secure_filename(file.filename)

        filepath = os.path.join(
            UPLOAD_FOLDER,
            filename
        )

        file.save(filepath)

        # -------------------------
        # CNN Prediction
        # -------------------------

        result = predict_image(filepath)

        disease = result["disease"]

        confidence = result["confidence"]

        # -------------------------
        # Temporary AI Explanation
        # -------------------------

        if disease == "NORMAL":

            summary = (
                "The uploaded chest X-ray appears normal. "
                "No significant signs of pneumonia were detected "
                "by the AI model."
            )

            precautions = (
                "Maintain a healthy lifestyle. "
                "Drink enough water. "
                "Exercise regularly. "
                "Consult a doctor if symptoms develop."
            )

        else:

            summary = (
                "The AI model detected possible signs of pneumonia. "
                "This prediction is generated by a deep learning model "
                "and should always be confirmed by a qualified doctor."
            )

            precautions = (
                "Consult a pulmonologist immediately. "
                "Take medicines only as prescribed. "
                "Stay hydrated. "
                "Take proper rest."
            )

        # -------------------------
        # Save to MySQL
        # -------------------------

        new_prediction = Prediction(

            user_id=current_user.id,

            image_path=filename,

            disease_name=disease,

            confidence_score=confidence,

            ai_summary=summary,

            precautions=precautions

        )

        db.session.add(new_prediction)

        db.session.commit()

        flash(
            "Prediction completed successfully.",
            "success"
        )

        return render_template(

            "prediction.html",

            prediction=new_prediction

        )

    return render_template("upload.html")